#include "BehaviorContext.h"


////////////////////////////////////////////////////////////////////////
///
/// @fn Behavior::changeBehavior()
///
/// Change le comportement actuel du robot
/// 
///
/// @param[in] : std::unique_ptr<Behavior> state
///
/// @return Aucune (constructeur).
///
////////////////////////////////////////////////////////////////////////
void BehaviorContext::changeBehavior(std::unique_ptr<Behavior> state)
{
	state_ = std::move(state);
}

////////////////////////////////////////////////////////////////////////
///
/// @fn Behavior::getRobot()
///
/// Change le comportement actuel du robot
/// 
///
/// @param[in] Aucun .
///
/// @return Aucune .
///
////////////////////////////////////////////////////////////////////////
NoeudRobot* BehaviorContext::getRobot() const
{
	return robot_;
}

////////////////////////////////////////////////////////////////////////
///
/// @fn Behavior::doAction()
///
/// Effectue une etape du comportement actuel
/// 
///
/// @param[in]
///
/// @return Aucune .
///
////////////////////////////////////////////////////////////////////////
void BehaviorContext::doAction()
{
	state_->doAction();
}