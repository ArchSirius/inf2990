////////////////////////////////////////////////////////////////////////
///
/// @fn void ScaleTool::defaultTranslate(NoeudAbstrait* node)
///
/// Implémentation du visiteur Mise à l'échelle par défaut.
///
/// @return Aucune.
///
////////////////////////////////////////////////////////////////////////
void ScaleTool::defaultScale(NoeudAbstrait* node)
{
	if (!node->estSelectionne() || !node->estSelectionnable())
		return;
}

